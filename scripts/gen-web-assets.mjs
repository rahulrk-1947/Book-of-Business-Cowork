// Generates browser-embeddable assets: the SQLite WASM binary (base64) and the schema SQL.
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';

mkdirSync('src/web/generated', { recursive: true });

const wasm = readFileSync('node_modules/sql.js/dist/sql-wasm.wasm');
writeFileSync(
  'src/web/generated/sqljs-wasm.ts',
  `// AUTO-GENERATED by scripts/gen-web-assets.mjs — do not edit.\nexport const SQLJS_WASM_BASE64 = ${JSON.stringify(wasm.toString('base64'))};\n`
);

const schema = readFileSync('src/backend/schema.sql', 'utf8');
writeFileSync(
  'src/web/generated/schema-sql.ts',
  `// AUTO-GENERATED by scripts/gen-web-assets.mjs — do not edit.\nexport const SCHEMA_SQL = ${JSON.stringify(schema)};\n`
);

console.log('web assets generated:', (wasm.length / 1024 / 1024).toFixed(2) + 'MB wasm,', (schema.length / 1024).toFixed(0) + 'KB schema');
